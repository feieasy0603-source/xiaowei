/** 根据论文标题生成演示用提纲正文（每次选用可带 seed 以区分批次） */
export function buildOutlineTextFromTitle(
  title: string,
  depth: 2 | 3,
  seed = 0,
): string {
  const topic = title.trim().slice(0, 36) || '本课题'
  const variant = seed % 2

  if (depth === 3) {
    if (variant === 0) {
      return `第一章 绪论
1.1 ${topic}的研究背景
1.1.1 选题背景
1.1.2 研究意义
1.2 国内外研究现状
1.2.1 国外研究现状
1.2.2 国内研究现状
1.3 研究内容与方法
第二章 理论基础
2.1 核心概念界定
2.2 理论框架
2.3 研究假设
第三章 ${topic}的实证分析
3.1 研究设计
3.2 数据分析
3.3 结果讨论
第四章 结论与展望
4.1 研究结论
4.2 不足与展望`
    }
    return `第一章 绪论
1.1 研究背景
1.1.1 现实背景
1.1.2 理论背景
1.2 文献综述
1.2.1 国外文献
1.2.2 国内文献
1.3 研究思路与技术路线
第二章 相关理论与模型
2.1 ${topic}相关理论
2.2 分析模型构建
第三章 研究设计与实施
3.1 数据来源
3.2 变量测度
3.3 实证检验
第四章 对策建议与结论
4.1 主要发现
4.2 对策建议
4.3 研究展望`
  }

  if (variant === 0) {
    return `第一章 绪论
1.1 ${topic}的研究背景
1.2 研究意义
1.3 研究内容与方法
第二章 理论基础与文献综述
2.1 相关概念界定
2.2 国内外研究现状
第三章 ${topic}的分析与对策
3.1 现状与问题分析
3.2 影响因素分析
3.3 对策建议
第四章 结论与展望
4.1 研究结论
4.2 研究展望`
  }

  return `第一章 绪论
1.1 研究背景
1.2 研究意义与目标
1.3 研究方法与技术路线
第二章 ${topic}的理论基础
2.1 基本概念
2.2 理论分析框架
第三章 实证与案例分析
3.1 研究对象与数据
3.2 结果分析
3.3 讨论
第四章 结论与建议
4.1 主要结论
4.2 实践建议`
}
